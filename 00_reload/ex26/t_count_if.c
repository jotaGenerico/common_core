int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	count;
	int	i;

	count = 0;
	i = 0;
	while (i < length)
	{
		if (tab[i] != NULL)
		{
			if (f(tab[i]) == 1)
				count += 1;
		}
		i++;
	}
	return (count);
}
