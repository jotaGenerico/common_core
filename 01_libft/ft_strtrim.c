#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*trimed;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end]))
		end--;
	if (start > end)
		return (ft_strdup(""));
	trimed = malloc(sizeof(char) * (end - start + 1));
	if (!trimed)
		return (NULL);
	ft_memcpy(trimed, s1 + start, end - start);
	trimed[end - start] = '\0';
	return (trimed);
}
